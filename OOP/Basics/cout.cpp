// C++ Standard Output Stream (cout):

/*
The cout is basically a predefined object of ostream class.
The cout object in conjunction with stream insertion operator (<<)
allows us to send data to standard output device(screen).
*/
// Example:-
#include <iostream>
using namespace std;

int main()
{
    cout << "Hello World!";
    return 0;
}

// Output:- Hello World!